int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	cnt;

	cnt = 0;
	if (n <= 0)
		return (0);
	while (*s1 == *s2 && cnt + 1 < n && !(*s1 == '\0' && *s2 == '\0'))
	{
		s1++;
		s2++;
		cnt++;
	}
	return ((int)(*s1 - *s2));
}
