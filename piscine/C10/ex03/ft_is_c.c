#include "ft_ex03.h"

int	ft_strcmp(char *s1, char *s2)
{
	while (*s1 == *s2 && !(*s1 == '\0' && *s2 == '\0'))
	{
		s1++;
		s2++;
	}
	return ((int)(*s1 - *s2));
}

int	ft_is_c(char *str)
{
	if (!ft_strcmp(str, "-C"))
		return (1);
	return (0);
}
