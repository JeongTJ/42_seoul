char	*ft_strlowcase(char *str)
{
	char	*res;

	res = str;
	while (1)
	{
		if (*str == '\0')
		{
			str = res;
			return (str);
		}
		if ('A' <= *str && *str <= 'Z')
			*str += 'a' - 'A';
		str++;
	}
}
