int	ft_str_is_alpha(char *str)
{
	int	flag;

	flag = 1;
	while (1)
	{
		if (*str == '\0')
			return (flag);
		if (!(('a' <= *str && *str <= 'z') || ('A' <= *str && *str <= 'Z')))
			flag = 0;
		str++;
	}
	return (flag);
}
