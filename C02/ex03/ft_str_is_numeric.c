int	ft_str_is_numeric(char *str)
{
	int	flag;

	flag = 1;
	while (1)
	{
		if (*str == '\0')
			return (flag);
		if (!('0' <= *str && *str <= '9'))
			flag = 0;
		str++;
	}
	return (flag);
}
